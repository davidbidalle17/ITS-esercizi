# Memorizzare un numero in virgola mobile nella variabile x
x = 3.0

# Calcolare 1.0/x e memorizzare il risultato nella variabile y
y = 1.0 / x

# Visualizzare il valore di x, y e il prodotto tra x e y
print("x:", x)
print("y:", y)
print("x * y:", x * y)

# Sottrarre x dal prodotto tra x e y e mostrare il risultato
result = (x * y) - x
print("Risultato di (x * y) - x:", result)

